//   1. Запрашивает у пользователя число.
//   2. Проверяет, является ли введенное значение числом.
//   3. Если значение не является числом, возвращает строку 
// 'Переданный параметр не является числом'.
//   4. Если значение является числом, возвращает строку 'n в кубе 
// равняется <получившееся значение>', где n — введенное число, 
// а  <получившееся значение> — число, возведенное в куб.
//   5. Проверьте работу функции с числами от  0 до 10.

function BunA() {
    let number = prompt('Введите число');
    if (isNaN(number)) {
        return 'Переданный параметр не является числом';
    } else {
        return `${number} в кубе равняется ${number ** 3}`;
    }
}
console.log(BunA());

